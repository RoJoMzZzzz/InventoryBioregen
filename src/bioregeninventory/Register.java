    private void registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerActionPerformed
    if((regID.getText().equals(""))||(regPass.getText()).equals("")||(regName.getText().equals(""))||(regPos.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Incomplete");
    }
        
        else {
        String sql = "Insert into userstbl (Employee_ID,Password,Complete_Name,Position) values (?,?,?,?)";
        try {
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, regID.getText());
            ps.setString(2, regPass.getText());
            ps.setString(3, regName.getText());
            ps.setString(4, regPos.getText());
            ps.execute();
            JOptionPane.showMessageDialog(null, "Registered");
            this.dispose();
            new Login().setVisible(true);
        }
        catch(SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_registerActionPerformed

